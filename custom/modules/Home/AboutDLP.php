  <li>
    Kijk regelmatig op de projectpagina om te kijken of er een nieuwere versie is
  </li>
  <li>
    Projectpagina: <a href="http://www.sugarforge.org/projects/dutch-madcap/" target="_blank">SugarForge Dutch Language Pack</a>
