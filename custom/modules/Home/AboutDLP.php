<h1>Nederlandse vertaling voor SuiteCRM</h1>
De Nederlandse vertaling van SuiteCRM wordt gratis beschikbaar gesteld door Hortindustrias (<a target="_blank" href="http://www.hortindustries.com">http://www.hortindustries.com</a>).
<ul>
  
  <li>
    Projectpagina: <a href="https://github.com/erikvanberkum/SuiteCRM_Dutch" target="_blank">Github Dutch Language Pack</a>
  </li>
</ul>

<h2>Onafhankelijke module namen</h2>
Deze vertaling is anders dan anders. De Nederlandse vertaling van SuiteCRM is
intelligent, in de zin dat module namen door de applicatie heen kunnen worden aangepast door op
&eacute;&eacute;n enkele plek een wijziging te maken. Dit in tegenstelling tot de andere talen,
waarbij wijzigingen door de applicatie heen moeten worden aangepast.<br><br>

<h2>Modules hernoemen</h2>
Om een module te hernoemen kunt u de opties in het Configuratie scherm gebruiken. U kunt zowel de
enkelvoudige, meervoudige als het geslacht van het woord opgeven en deze zullen door heel SuiteCRM
heen worden gebruikt.
